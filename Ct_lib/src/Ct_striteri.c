#include "../inc/Ct_lib.h"

void	Ct_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i++])
		f(i, &s[i]);
}
